<template>
  <div id="app">
    <router-view/>
  </div>
</template>

<script>
import { mapState } from 'vuex'

export default {
  name: 'App',
  computed: {
    ...mapState('user', ['token'])
  },
  created() {
    // 如果有token，则获取用户信息
    if (this.token) {
      this.$store.dispatch('user/fetchUserInfo')
    }
  }
}
</script>

